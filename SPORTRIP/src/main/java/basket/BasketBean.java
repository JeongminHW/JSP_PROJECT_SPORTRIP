package basket;

public class BasketBean {
    private int BASKET_NUM;
    private String ID;
    private int MD_NUM;
    private int REPAIR_B;
	public int getBASKET_NUM() {
		return BASKET_NUM;
	}
	public void setBASKET_NUM(int bASKET_NUM) {
		BASKET_NUM = bASKET_NUM;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public int getMD_NUM() {
		return MD_NUM;
	}
	public void setMD_NUM(int mD_NUM) {
		MD_NUM = mD_NUM;
	}
	public int getREPAIR_B() {
		return REPAIR_B;
	}
	public void setREPAIR_B(int rEPAIR_B) {
		REPAIR_B = rEPAIR_B;
	}
    
    
}