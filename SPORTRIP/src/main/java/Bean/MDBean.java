package Bean;

public class MDBean {
    private int mdNum;
    private int teamNum;
    private int sportNum;
    private String mdName;
    private int mdPrice;
    private String mdKindOf;
    private String mdImg;

    public MDBean() {}

    public int getMdNum() {
        return mdNum;
    }

    public void setMdNum(int mdNum) {
        this.mdNum = mdNum;
    }

    public int getTeamNum() {
        return teamNum;
    }

    public void setTeamNum(int teamNum) {
        this.teamNum = teamNum;
    }

    public int getSportNum() {
        return sportNum;
    }

    public void setSportNum(int sportNum) {
        this.sportNum = sportNum;
    }

    public String getMdName() {
        return mdName;
    }

    public void setMdName(String mdName) {
        this.mdName = mdName;
    }

    public int getMdPrice() {
        return mdPrice;
    }

    public void setMdPrice(int mdPrice) {
        this.mdPrice = mdPrice;
    }

    public String getMdKindOf() {
        return mdKindOf;
    }

    public void setMdKindOf(String mdKindOf) {
        this.mdKindOf = mdKindOf;
    }

    public String getMdImg() {
        return mdImg;
    }

    public void setMdImg(String mdImg) {
        this.mdImg = mdImg;
    }
}